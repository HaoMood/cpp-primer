#ifndef ARTH_H_
#define ARTH_H_

inline int add(const int a, const int b)
{
    return a + b;
}

inline int sub(const int a, const int b)
{
    return a - b;
}

inline int mul(const int a, const int b)
{
    return a * b;
}

inline int divide(const int a, const int b)
{
    return a / b;
}

#endif
